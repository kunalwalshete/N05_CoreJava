package org.tns.wrapperclassdemo;

// Program to demonstrate on unboxing
// Unboxing is conversion of object type to primitive type
public class Unboxing {

	public static void main(String[] args) {
		
		Integer num =5;         // object type
		
		int x = num;         // Converting object type to primitive data type
		
		System.out.println(x);

	}

}
