package org.tns.wrapperclassdemo;

// Program to demonstrate on Autoboxing
// Autoboxing = It is the conversation of primitive type to object type
public class Autoboxing {

	public static void main(String[] args) {
		
		char c = 'A';    // Primitive data type
		
		Character ch =c;   // converting primitive data type into object type

		System.out.println(ch);
	}

}
